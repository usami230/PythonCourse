
# type casting (or conversion)
"""
Definition: The conversion of one data type into the other data type is known as type casting in python or type conversion in python.
"""

# type conversion functions
# str()
# int()
# float()
# bool()

# convert str to int
x = "500"
print(type(x))  # <class 'str' >
x = int(x)      # convert str to int
print(type(x))  # <class 'int' >


# convert float to int
y = 500.2859
print(type(y))  # <class 'float' >
y = int(y)      # convert float to int
print(type(y))  # <class 'int' >


# convert int to float
z = 500
print(type(z))  # <class 'int' >
z = int(z)      # convert int to float
print(type(z))  # <class 'float' >


# convert int to str
a = 500
print(type(a))  # <class 'int' >
a = str(a)      # convert int to str
print(type(a))  # <class 'str' >
